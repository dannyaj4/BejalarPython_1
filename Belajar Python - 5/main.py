# Operator Aritmatika

a = 3
b = 4

#Penambahan
tambah = a + b
print(a, " + ", b, "=", tambah)

#Pengurangan 
kurang = a - b
print(a, " - ", b, "=", kurang)

#Pembagian
bagi = a / b
print(a, " / ", b, "=", bagi)

#Perkalian
kali = a * b
print(a, " * ", b, "=", kali)

#Eksopnen / pangkat
pangkat = a ** b
print(a, " ** ", b, "=", pangkat)

#Modulus
modulus = a % b
print(a, " * ", b, "=", modulus)

#Operasi floor devision 
o_fd = a // b
print(a, " // ", b, "=", o_fd)

